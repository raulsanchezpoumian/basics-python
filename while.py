# Three examples of while

# Example #1
count = 0
while count < 10:
    print(count)
    count += 1

# Example #2
import random

number = 0
while number <= 8:
    number = random.randint(1, 10)
    print(number)


# Example #3
message = "Hello, world!"
count = 0
while count < 3:
    print(message)
    count += 1