
addition = 10 + 5 # Esto dará como resultado 15
substraction = 10 - 5 # Esto dará como resultado 5
multiplication = 10 * 5 # Esto dará como resultado 50

### Comparision operators
age = 10 == 5 # Esto dará como resultado False
age = 10 != 5 # Esto dará como resultado True
age = 10 > 5 # Esto dará como resultado True

### Logical Operators
age_boy = 10
age_girl = 14

