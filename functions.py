# Python Functions

# Example #1
def add_numbers(x, y):
    return x + y

result = add_numbers(5, 3)
print(result) #8

# Example #2
def multiply_numbers(x, y):
    return x * y

result = multiply_numbers(5, 3)
print(result) #15

# Example #3
def substract_numbers(x, y):
    return x - y

result = substract_numbers(5, 3)
print(result) #2

