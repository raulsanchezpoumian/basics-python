# Three examples of If...else statements

# Example #1
distance_travel = input("Enter the distance to travel in kilometers: ")
if int(distance_travel) > 300:
    print("Take an airplane flight")
else:
    print("Drive your car or take a bus")


# Example 2
number = input("Please enter a number: ")
if int(number) > 0:
    print("Your number is positive")
else:
    print("Your number is Negative")


# Example 3
shoot_distance = input("Enter the distance of the basketball shot: ")
if float(shoot_distance) < 23.9:
    print("Your basket is a 2 point shot")
else:
    print("Your basket is a 3 point shot")

